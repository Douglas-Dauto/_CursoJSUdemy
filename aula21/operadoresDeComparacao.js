/*
    Operadores de Comparação
    > Maior que
    >= Maior que ou igual a
    < Menor que
    <= Menor que ou igual a
    == Igualdade // Checa apenas o valor
    === Igualdade estrita // Checa o valor e o tipo
    != Diferente // Checa apenas o valor
    !== Diferente estrito // Checa o valor e o tipo
*/
// Utilize sempre o === e !== ao invés de == e !=
// O resultado sempre será um valor booleano.

console.log(5 > 4); // Saida == true
console.log(8 >= 8); // Saida == true
console.log(9 !== 9); // Saida == false
console.log(1 === '1'); // Saida == false
